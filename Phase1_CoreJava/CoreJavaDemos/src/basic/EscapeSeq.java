package basic;

public class EscapeSeq {

	public static void main(String[] args) {
		// escape sequences => are special characters that start with \ followed 
		// by a single character without space
		
		// he said, " Let's go out"
		System.out.println(" He said, \"Let's go out\" ");
		System.out.println("Hey there\ncome out");
		System.out.println("Name\tEmail");

	}

}
